public class Kalkulyator {

    public int vurma(int a, int b){
        System.out.print("2 tam ededin vurulmasi: ");
        return a*b;
    }
    public int vurma(int a, int b, int c){
        System.out.print("3 tam ededin vurulmasi: ");
        return a*b*c;
    }
    public double vurma(double a, double b){
        System.out.print("2 kesr ededin vurulmasi: ");
        return a*b;
    }
}
