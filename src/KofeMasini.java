public class KofeMasini {
    public void kofeHazirla(){
        System.out.println("Kofe hazırlanmağa başladı");
        suQizdir();
        kofeTokmek();
        fincanDoldu();
    }
    private void suQizdir(){
        System.out.println("Su qızdırıldı");
    }
    private void kofeTokmek(){
        System.out.println("Kofe əlavə olundu");
    }
    private void fincanDoldu(){
        System.out.println("Kofe fincanı hazırdır");
    }
}