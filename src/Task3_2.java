public class Task2 {
    public static void main(String[] args) {
        int x = 36, y = 6;

        System.out.println(x+y);   // → 42
        System.out.println(x-y);   // → 30
        System.out.println(x*y);   // → 216
        System.out.println(x/y);   // → 6
        System.out.println(x%y);   // → 0
    }
}
